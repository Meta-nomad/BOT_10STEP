def question_text(fmt_key, idx):
    qs = FORMATS[fmt_key]["qs"]
    item = qs[idx]
    total = len(qs)
    is_last = (idx == total - 1)
    text = (
        f"*{item['s']}*\n"
        f"_{idx + 1} из {total}_\n\n"
        f"{item['q']}\n\n"
        f"_Напиши ответ 👇_"
    )
    return text, is_last


async def send_question(target, context, is_msg=False):
    """Показать текущий вопрос. target = message или query."""
    ud = context.user_data
    text, is_last = question_text(ud["fmt"], ud["idx"])
    kb = kb_skip(is_last)
    if is_msg:
        await target.reply_text(text, parse_mode="Markdown", reply_markup=kb)
    else:
        await target.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
    ud["wait"] = True


