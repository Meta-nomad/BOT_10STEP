async def cmd_start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data.clear()
    await update.message.reply_text(WELCOME, parse_mode="Markdown", reply_markup=kb_main())
    return MENU


async def menu_cb(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q_ = update.callback_query
    await q_.answer()
    cb = q_.data

    if cb == "new":
        lines = ["*Выбери формат:*\n"]
        for v in FORMATS.values():
            lines.append(f"*{v['name']}*\n_{v['desc']}_\n")
        await q_.edit_message_text("\n".join(lines), parse_mode="Markdown",
                                   reply_markup=kb_formats())
        return PICK

    if cb == "info":
        await q_.edit_message_text(INFO, parse_mode="Markdown", reply_markup=kb_back())
        return MENU

    if cb == "hist":
        entries = db_entries(q_.from_user.id)
        if not entries:
            await q_.edit_message_text("📭 Записей пока нет.", reply_markup=kb_back())
            return MENU
        last5 = entries[-5:][::-1]
        text = "📚 *Последние записи:*\n\n"
        for e in last5:
            short = FORMATS.get(e["fmt"], {}).get("short", "?")
            ans = sum(1 for a in e.get("answers", []) if a)
            text += f"• {short} · {e['date']} · {ans}/{e.get('total','?')}\n"
        # Показываем последнюю запись
        e = last5[0]
        fmt = FORMATS.get(e["fmt"], {})
        qs = fmt.get("qs", [])
        text += "\n*Последняя запись:*\n"
        for i, ans in enumerate(e.get("answers", [])):
            if ans and i < len(qs):
                q_short = qs[i]["q"].split("\n")[0][:50]
                text += f"\n❓ _{q_short}…_\n✍️ {ans}\n"
        await q_.edit_message_text(text, parse_mode="Markdown", reply_markup=kb_back())
        return MENU

    if cb == "clear":
        db_clear(q_.from_user.id)
        await q_.edit_message_text("🗑 История очищена.", reply_markup=kb_main())
        return MENU

    if cb == "back":
        await q_.edit_message_text(WELCOME, parse_mode="Markdown", reply_markup=kb_main())
        return MENU

    return MENU


async def pick_cb(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q_ = update.callback_query
    await q_.answer()
    cb = q_.data

    if cb == "back":
        context.user_data.clear()
        await q_.edit_message_text(WELCOME, parse_mode="Markdown", reply_markup=kb_main())
        return MENU

    fmt_key = cb.removeprefix("f_")
    if fmt_key not in FORMATS:
        return PICK

    context.user_data.update({"fmt": fmt_key, "idx": 0, "ans": {}, "wait": False})
    await send_question(q_, context, is_msg=False)
    return WORK


async def work_cb(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q_ = update.callback_query
    await q_.answer()
    cb = q_.data
    ud = context.user_data

    if cb == "abort":
        context.user_data.clear()
        await q_.edit_message_text("❌ Прервано.", reply_markup=kb_main())
        return MENU

    if cb == "skip":
        # Пропускаем текущий вопрос — записываем пустой ответ
        ud["ans"][ud["idx"]] = ""
        ud["idx"] += 1
        qs = FORMATS[ud["fmt"]]["qs"]
        if ud["idx"] >= len(qs):
            return await finish(q_, context, is_msg=False)
        await send_question(q_, context, is_msg=False)
        return WORK

    return WORK


async def work_text(update: Update, context: ContextTypes.DEFAULT_TYPE):
    ud = context.user_data
    if not ud.get("wait"):
        return WORK

    # Сохраняем ответ
    ud["ans"][ud["idx"]] = update.message.text.strip()
    ud["wait"] = False
    ud["idx"] += 1

    qs = FORMATS[ud["fmt"]]["qs"]
    if ud["idx"] >= len(qs):
        return await finish(update.message, context, is_msg=True)

    # Сразу показываем следующий вопрос — без промежуточных экранов
    await send_question(update.message, context, is_msg=True)
    return WORK


async def cmd_cancel(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data.clear()
    await update.message.reply_text("Главное меню.", reply_markup=kb_main())
    return MENU


