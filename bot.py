def main():
    token = os.environ.get("BOT_TOKEN", "YOUR_BOT_TOKEN_HERE")
    app = Application.builder().token(token).build()

    conv = ConversationHandler(
        entry_points=[CommandHandler("start", cmd_start)],
        states={
            MENU: [CallbackQueryHandler(menu_cb)],
            PICK: [CallbackQueryHandler(pick_cb)],
            WORK: [
                CallbackQueryHandler(work_cb),
                MessageHandler(filters.TEXT & ~filters.COMMAND, work_text),
            ],
        },
        fallbacks=[CommandHandler("cancel", cmd_cancel)],
        allow_reentry=True,
        conversation_timeout=3600,  # сессия живёт 1 час без активности
    )

    app.add_handler(conv)
    print("🤖 Бот запущен. Ctrl+C для остановки.")
    app.run_polling(drop_pending_updates=True)


if __name__ == "__main__":
    main()
