async def finish(target, context, is_msg=False):
    ud = context.user_data
    fmt_key = ud["fmt"]
    answers = ud["ans"]          # dict {idx: text}
    qs = FORMATS[fmt_key]["qs"]
    total = len(qs)
    answered = sum(1 for v in answers.values() if v)

    db_append(target.from_user.id, {
        "fmt": fmt_key,
        "date": datetime.now().strftime("%d.%m.%Y %H:%M"),
        "answers": [answers.get(i, "") for i in range(total)],
        "total": total,
    })

    short = FORMATS[fmt_key]["short"]
    pct = answered / total * 100 if total else 0
    text = (
        f"✅ *Готово!*\n\n"
        f"📋 {short}\n"
        f"📅 {datetime.now().strftime('%d.%m.%Y %H:%M')}\n"
        f"✍️ Ответов: {answered} из {total} ({pct:.0f}%)\n\n"
        + ("_Хорошая работа. 10-й Шаг сделан. 💙_" if answered > 0
           else "_Ты открыл(а) бот. Завтра попробуй ответить хотя бы на один вопрос._")
    )
    context.user_data.clear()
    if is_msg:
        await target.reply_text(text, parse_mode="Markdown", reply_markup=kb_main())
    else:
        await target.edit_message_text(text, parse_mode="Markdown", reply_markup=kb_main())
    return MENU


