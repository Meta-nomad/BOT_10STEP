# ── БД ────────────────────────────────────────────────────────────────────────
def db_load():
    try:
        with open(DATA_FILE, encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return {}

def db_save(data):
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def db_append(uid, entry):
    data = db_load()
    data.setdefault(str(uid), []).append(entry)
    db_save(data)

def db_entries(uid):
    return db_load().get(str(uid), [])

def db_clear(uid):
    data = db_load()
    data[str(uid)] = []
    db_save(data)


