def kb_skip(is_last):
    row = [InlineKeyboardButton(
        "⏭ Пропустить и завершить" if is_last else "⏭ Пропустить",
        callback_data="skip"
    )]
    return InlineKeyboardMarkup([row,
        [InlineKeyboardButton("❌ Прервать", callback_data="abort")]])

